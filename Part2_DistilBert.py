def evaluate_model(model, dataloader, device, use_amp: bool = False):
    """Return metrics, confusionâ€‘matrix, classificationâ€‘report, preds, labels."""
    model.eval()
    total_loss = 0.0
    all_preds, all_labels = [], []

    progress_bar = tqdm(dataloader, desc="Evaluating", leave=False)

    with torch.no_grad():
        for batch in progress_bar:
            batch = {k: v.to(device) for k, v in batch.items()}
            labels = batch["labels"]

            try:
                with autocast(device_type=device.type, enabled=use_amp):
                    outputs = model(**batch)
                    loss = outputs.loss
                    logits = outputs.logits

                total_loss += loss.item()
                all_preds.extend(torch.argmax(logits, dim=1).cpu().numpy())
                all_labels.extend(labels.cpu().numpy())

            except Exception as e:
                print(f"\nError during eval batch: {e}")
                continue

    # handle edgeâ€‘case where no labels were collected
    if not all_labels:
        dummy_metrics = {
            "loss": float("nan"),
            "accuracy": 0.0,
            "f1": 0.0,
            "precision": 0.0,
            "recall": 0.0,
        }
        return dummy_metrics, np.array([]), "Evaluation failed", [], []

    avg_loss = total_loss / len(dataloader)
    accuracy = accuracy_score(all_labels, all_preds)
    precision, recall, f1, _ = precision_recall_fscore_support(
        all_labels, all_preds, average="binary", zero_division=0
    )

    cm = confusion_matrix(all_labels, all_preds)
    cr = classification_report(
        all_labels,
        all_preds,
        target_names=["Neg(0)", "Pos(1)"],
        digits=4,
        zero_division=0,
        output_dict=False,
    )

    metrics = {
        "loss": avg_loss,
        "accuracy": accuracy,
        "f1": f1,
        "precision": precision,
        "recall": recall,
    }

    return metrics, cm, cr, all_labels, all_preds


