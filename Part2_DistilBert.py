"""###Â Master Training Function  
- **Runâ€‘orchestrator `run_training_experiment()`** â€“ wraps an entire DistilBERT fineâ€‘tuning cycle (optionally LoRAâ€‘adapted) into one call: it accepts small dicts describing *model*, *training*, and *data* knobs plus a `DatasetDict`, tokenizer, device, and a shared `results_list` to which it appends a selfâ€‘contained run record.  

- **Experiment bookkeeping & paths** â€“ immediately prints a JSON snapshot of every knob, then creates experimentâ€‘specific subâ€‘folders for weights, plots, and CLSâ€‘embeddings (`models_reports/`, `plots/`, `cls_embeddings/`) under the supplied base artifact dir.  

- **Preâ€‘processing & loaders** â€“ tokenizes the chosen text column via `prep_tok_ds()`, instantiates train/val/test loaders (dynamic padding, CUDA pinâ€‘memory), and aborts early if any step fails.  

- **Model provisioning** â€“  
  * Builds a fresh `DistilBertConfig` with custom dropout and numâ€‘labels.  
  * Loads pretrained weights, freezes layers according to `freeze_level` *or* inserts a PEFT LoRA adapter (configurable `r`,Â `alpha`, targets).  
  * Reports the exact trainable parameter count.  

- **Optimization stack** â€“ selects AdamW over *trainable* params only, derives total/ warmâ€‘up steps for a HuggingÂ Face scheduler, enables mixedâ€‘precision with `GradScaler` when CUDA+AMP are allowed, and logs the chosen LR / WD.  

- **Training loop** â€“ for each epoch:  
  1. Batches are cast to device; forward pass is optionally autocast.  
  2. Loss backward, gradientâ€‘clip (if set), scalerâ€‘step, schedulerâ€‘step.  
  3. Tracks perâ€‘batch loss/acc & LR into `batch_hist`; perâ€‘epoch aggregates into `hist`.  
  4. Evaluates on the validation split, prints metrics, performs patienceâ€‘based earlyâ€‘stopping, and checkpoints the best weights (LoRA adapter or `.pt`).  

- **Postâ€‘training evaluation** â€“ reloads the *best* checkpoint (PEFTâ€‘aware) onto the same device, runs fullâ€‘test evaluation via `evaluate_model()`, prints metrics, and writes a plainâ€‘text `experiment_report.txt` capturing config + val/test scores + confusion matrix + classification report.  

- **Downâ€‘stream features** â€“ extracts `[CLS]` embeddings for train/val/test with `extract_cls_embeddings()` and persists them as `.npy`, enabling classical models or visualization later.  

- **Visualization** â€“ autoâ€‘plots (1) epochâ€‘level loss/acc, (2) stepâ€‘level smoothed metrics with LR curve and epoch/best markers, and (3) lossâ€‘vsâ€‘LR log plot; PNGs land in the experimentâ€™s plot folder.  

- **Result object & cleanup** â€“ appends a dict summarizing status, timings, best epoch, and metrics to the shared `results_list`, aggressively garbageâ€‘collects GPU/CPU memory, and returns the same dict for immediate use.
"""

