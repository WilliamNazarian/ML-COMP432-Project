# tokenize a batch of examples
def tokenize_batch(examples, column):
    """Tokenize a list of texts under `column`."""
    texts = [str(t) if t else "" for t in examples[column]]
    return tokenizer(
        texts,
        max_length=CONFIG["MAX_LENGTH"],
        truncation=True,
        padding=False,
    )

